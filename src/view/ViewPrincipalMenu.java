    private void txtPrecioProductoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPrecioProductoKeyReleased

        try {
            NProduct productController = new NProduct();
            if (txtPrecioProducto.getText().isEmpty()) {
                txtPrecioIvaProducto.setText("0.0");
            } else {
                Double calcPriceDouble = Double.parseDouble(txtPrecioProducto.getText()) * Double.parseDouble(productController.consultIva());
                txtPrecioIvaProducto.setText(String.valueOf((double) Math.round(calcPriceDouble * 1000d) / 1000));
            }
        } catch (Exception e) {
            txtPrecioIvaProducto.setText("0.0");
        }
    }//GEN-LAST:event_txtPrecioProductoKeyReleased

