    private void btnEditarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarClienteActionPerformed

        if (txtCedulaCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Identificacin", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (cmbTipoCliente.getSelectedItem().toString().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Seleccione el Tipo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtTelefonoCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el numero de Telefono", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtNombresCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Nombre", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtDeudaCliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el valor de la deuda. En caso de no haber deuda ingrese '0'", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else {
            //EDITAR VALORES A LA TABLA CLIENTE
            DAOClient client = new DAOClient();

            NClient clientController = new NClient();

            client.setClientID(Integer.parseInt(txtCodigoCiente.getText()));
            client.setDni(txtCedulaCliente.getText());
            client.setType(cmbTipoCliente.getSelectedItem().toString());
            client.setName(txtNombresCliente.getText());
            client.setLastName(txtApellidosCliente.getText());
            client.setAddress(txtDireccionCliente.getText());
            client.setPhoneNumber(txtTelefonoCliente.getText());
            client.setEmail(txtEmailCliente.getText());
            client.setDebt(txtDeudaCliente.getText());

            clientController.edit(client);

            //MENSAJE
            JOptionPane.showMessageDialog(this, "Los datos han sido modificados", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            showClient("");
            //BORAR DATO DE LA CAJA DE TEXTOS

            cleanClient();

            enableClient();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarClienteActionPerformed

