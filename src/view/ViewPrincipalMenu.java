    private void tblListadoEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblListadoEmpleadoMouseClicked

        cleanEmployee();

        int fila = tblListadoEmpleado.rowAtPoint(evt.getPoint());

        try {
            txtCodigoEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 0).toString());
        } catch (NullPointerException nullE) {
            txtCodigoEmpleado.setText("NULL");
        }
        try {
            txtCedulaEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 1).toString());
        } catch (NullPointerException nullE) {
            txtCedulaEmpleado.setText("NULL");
        }
        try {
            txtNombresEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 3).toString());
        } catch (NullPointerException nullE) {
            txtNombresEmpleado.setText("NULL");
        }
        try {
            txtApellidosEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 4).toString());
        } catch (NullPointerException nullE) {
            txtApellidosEmpleado.setText("NULL");
        }
        try {
            txtDireccionEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 5).toString());
        } catch (NullPointerException nullE) {
            txtDireccionEmpleado.setText("NULL");
        }
        try {
            txtTelefonoEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 6).toString());
        } catch (NullPointerException nullE) {
            txtTelefonoEmpleado.setText("NULL");
        }
        try {
            txtEmailEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 7).toString());
        } catch (NullPointerException nullE) {
            txtEmailEmpleado.setText("NULL");
        }
        try {
            txtSueldoEmpleado.setText(tblListadoEmpleado.getValueAt(fila, 8).toString());
        } catch (NullPointerException nullE) {
            txtSueldoEmpleado.setText("NULL");
        }
        if (tblListadoEmpleado.getValueAt(fila, 2).toString().equals("NATURAL")) {
            cmbTipoEmpleado.setSelectedItem(String.valueOf("NATURAL"));
        } else {
            cmbTipoEmpleado.setSelectedItem(String.valueOf("JURIDICA"));
        }
        if (tblListadoEmpleado.getValueAt(fila, 9).toString().equals("1")) {
            cmbEstadoEmpleado.setSelectedItem(String.valueOf("ACTIVO"));
        } else {
            cmbEstadoEmpleado.setSelectedItem(String.valueOf("INACTIVO"));
        }

        disableEmployee();

        // TODO add your handling code here:
    }//GEN-LAST:event_tblListadoEmpleadoMouseClicked

