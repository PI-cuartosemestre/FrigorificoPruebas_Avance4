    private void btnGuardarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarEmpleadoActionPerformed

        if (txtCedulaEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese Identificacin", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (cmbTipoEmpleado.getSelectedItem().toString().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Seleccione un Tipo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtTelefonoEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el telefono", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtNombresEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el Nombres", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtSueldoEmpleado.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el sueldo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (cmbEstadoEmpleado.getSelectedItem().toString().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Especifique si el empleado se encuentra activo o inactivo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else {

            //INSERTAR VALORES A LA TABLA EMPLEADO
            DAOEmployee employee = new DAOEmployee();

            NEmployee employeeController = new NEmployee();

            employee.setDni(txtCedulaEmpleado.getText());
            employee.setType(cmbTipoEmpleado.getSelectedItem().toString());
            employee.setName(txtNombresEmpleado.getText());
            employee.setLastName(txtApellidosEmpleado.getText());
            employee.setAddress(txtDireccionEmpleado.getText());
            employee.setPhoneNumber(txtTelefonoEmpleado.getText());
            employee.setEmail(txtEmailEmpleado.getText());
            employee.setSalary(txtSueldoEmpleado.getText());
            if (cmbEstadoEmpleado.getSelectedItem().toString().equals("ACTIVO")) {
                employee.setState("1");
            } else {
                employee.setState("0");
            }

            employeeController.insertPerson(employee);
            employeeController.insertEmpleado(employee);

            //MENSAJE
            JOptionPane.showMessageDialog(this, "Los datos han sido guardados", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            showEmployee("");

            // BORAR DATO DE LA CAJA DE TEXTOS
            cleanEmployee();
            enableEmployee();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnGuardarEmpleadoActionPerformed

