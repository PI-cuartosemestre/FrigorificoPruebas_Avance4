    private void listadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listadoMouseClicked

        limpiar();

        int fila = listado.rowAtPoint(evt.getPoint());

        try {
            txtCodigoProveedor.setText(listado.getValueAt(fila, 0).toString());
        } catch (NullPointerException nullE) {
            txtCodigoProveedor.setText("NULL");
        }
        if(listado.getValueAt(fila, 1).toString().equals("NATURAL")){
            cmbTipo.setSelectedItem(String.valueOf("NATURAL"));
        }else{
            cmbTipo.setSelectedItem(String.valueOf("JURIDICA"));
        }
        try {
            txtNombres.setText(listado.getValueAt(fila, 2).toString());
        } catch (NullPointerException nullE) {
            txtNombres.setText("NULL");
        }
        try {
            txtApellidos.setText(listado.getValueAt(fila, 3).toString());
        } catch (NullPointerException nullE) {
            txtApellidos.setText("NULL");
        }
        try {
            txtTelefono.setText(listado.getValueAt(fila, 4).toString());
        } catch (NullPointerException nullE) {
            txtTelefono.setText("NULL");
        }
        try {
            txtEmail.setText(listado.getValueAt(fila, 5).toString());
        } catch (NullPointerException nullE) {
            txtEmail.setText("NULL");
        }
        try {
            txtDireccion.setText(listado.getValueAt(fila, 6).toString());
        } catch (NullPointerException nullE) {
            txtDireccion.setText("NULL");
        }
        if(listado.getValueAt(fila, 7).toString().equals("1")){
            cmbEstado.setSelectedItem(String.valueOf("VIGENTE"));
        }else{
            cmbEstado.setSelectedItem(String.valueOf("INACTIVO"));
        }
        try {
            txtCedula.setText(listado.getValueAt(fila, 8).toString());
        } catch (NullPointerException nullE) {
            txtCedula.setText("NULL");
        }
        try {
            txtidpersona.setText(listado.getValueAt(fila, 10).toString());
        } catch (NullPointerException nullE) {
            txtDireccion.setText("NULL");
        }
        try {
            txtidproveedor.setText(listado.getValueAt(fila, 9).toString());
        } catch (NullPointerException nullE) {
            txtDireccion.setText("NULL");
        }
        desahabilitar();

        // TODO add your handling code here:
    }//GEN-LAST:event_listadoMouseClicked

