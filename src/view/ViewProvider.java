    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed

        if (txtCedula.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese la Identificacion ", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtCodigoProveedor.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el Codigo de Proveedor", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (cmbTipo.getSelectedItem().toString().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Seleccione el Tipo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtNombres.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el Nombre", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el correo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (cmbEstado.getSelectedItem().toString().equals("Seleccionar")) {
            JOptionPane.showMessageDialog(this, "Especifique si el empleado se encuentra activo o inactivo", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else {

            DAOProvider provider = new DAOProvider();

            NProvider providerController = new NProvider();

            // provider.setIdcliente(Integer.parseInt(txtCodigo.getText()));
            provider.setProviderCode(txtCodigoProveedor.getText());
            provider.setDni(txtCedula.getText());
            provider.setType(cmbTipo.getSelectedItem().toString());
            provider.setName(txtNombres.getText());
            provider.setLastName(txtApellidos.getText());
            provider.setAddress(txtDireccion.getText());
            provider.setPhoneNumber(txtTelefono.getText());
            provider.setEmail(txtEmail.getText());
            if(cmbEstado.getSelectedItem().toString().equals("VIGENTE")){
                provider.setState("1");
            }else{
                provider.setState("0");
            }
            providerController.insertPerson(provider);
            providerController.insertProvider(provider);

            JOptionPane.showMessageDialog(this, "Los datos han sido guardados", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            mostrar("");
            limpiar();
            habilitar();
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_btnguardarActionPerformed

