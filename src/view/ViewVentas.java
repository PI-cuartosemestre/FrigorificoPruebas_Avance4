    private void totales() {

        NProduct productController = new NProduct();
        String pre;
        String can;
        double iva = 0;
        double total = 0;
        double subtotal = 0;
        double precio;
        double cantidad;
        double imp = 0.0;
        String valorIva = productController.consultIva().substring(2, 4);

        for (int i = 0; i < tbdetalle.getRowCount(); i++) {
            pre = tbdetalle.getValueAt(i, 2).toString();
            can = tbdetalle.getValueAt(i, 3).toString();

            precio = Double.parseDouble(pre);
            cantidad = Double.parseDouble(can);

            imp = precio * cantidad;

            subtotal = subtotal + imp;
            iva = subtotal * Double.parseDouble("0." + valorIva);
            System.out.println(iva);
            total = subtotal + iva;

            tbdetalle.setValueAt(Math.rint(imp * 100) / 100, i, 4);

        }

        txtsubtotal.setText(Double.toString((double) Math.round(subtotal * 1000d) / 1000));
        txtIva.setText("" + Math.rint(iva * 100) / 100);
        txttotal.setText("" + Math.rint(total * 100) / 100);

    }

    void incrementar() {

        //  NEGOCIO 
        NVenta auto = new NVenta();

        // VARIABLE  LLAMAR AL AUTOGENERAR VENTA   DE   NEGOCIO   NVENTA
        String d = auto.Autogenerar();

        //  codigo  variable  
        String codigo = auto.Autogenerar();
        //String es para poder autogenerar   0001  0002   pero como el tipo de dato tabla venta esta int 
        // por eso se come 000  y solo guardar  numero  1 

        // si el tipo de dato fuera char o varchar  hay si guardaria el cero 0001   0002
        // empieza nulo
        if (codigo == null) {

            // empieza del   0001  como referencia  puede ser otro p    x  etc
            txtid.setText("1");

        } else {

            // si ya exite un registro entonces sigue el siguiente  numero  2
            int increment = Integer.parseInt(codigo);

            increment = increment + 1;

            txtid.setText("" + increment);
        }

    }

    void guardarDetalle() {

        DAOVentas v = new DAOVentas();

        NVenta f = new NVenta();

        for (int i = 0; i < tbdetalle.getRowCount(); i++) {

            int fa = Integer.parseInt(txtid.getText());

            String inventario = (tbdetalle.getValueAt(i, 0).toString());

            String cantidad = (tbdetalle.getValueAt(i, 3).toString());

            String precio = (tbdetalle.getValueAt(i, 2).toString());

            String totales = (tbdetalle.getValueAt(i, 4).toString());

            v.setCodigoVenta(fa);

            v.setInventario(inventario);

            v.setCantidad(cantidad);

            v.setValor_unitario(precio);

            v.setTotal(totales);

            f.detalle(v);
        }

    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if (txtcodcliente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Seleccione un cliente en el boton '+' ", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtsubtotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese los Datos", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else if (txtcodigo.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese los Datos", "Aviso", JOptionPane.ERROR_MESSAGE);

        } else {

            //   CABECERA 
            DAOVentas v = new DAOVentas();

            NVenta f = new NVenta();

            v.setCodigoVenta(Integer.parseInt(txtid.getText()));

            v.setNumero(txtcodcliente.getText());

            v.setFecha(txtfecha.getText());

            v.setSubTotal(txtsubtotal.getText());

            v.setIva(txtIva.getText());

            v.setTotal(txttotal.getText());

            v.setPago(txtcodpago.getText());

            f.insertar(v);

            /////****DETALLE******************************
            guardarDetalle();

            incrementar();

            limpiar();

            txtcantidad.setEnabled(false);

            btnAgregarProductoVenta.setEnabled(false);

            jButton1.setEnabled(false);

            dispose();

            ViewVentas X = new ViewVentas();
            X.setVisible(true);

    }//GEN-LAST:event_jButton1ActionPerformed
    }
